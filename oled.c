void oled_print_char(char c) {
     uint8_t i;
    for (i = 0; i < 6; i++) {
        oled_data(characters[(c - 32) * 6 + i]);
    }
}

void oled_print(char *str) {
    while (*str) {
        oled_print_char(*str++);
    }
}

void oled_clear(void)
{
    uint8_t page;
    for ( page = 0; page < 8; page++)
    {
        oled_command(0xB0 + page);    // Set page address
        oled_command(0x00);           // Set lower column address
        oled_command(0x10);           //higher column address
        uint8_t column;
        for (column = 0; column < 128; column++)
    {
            oled_data(0x00);         //blank
    }
}
}
void oled_setcursor(uint8_t row, uint8_t col) {
    // Page address (0-7 for 128x64 displays)
// Column address (0-127 for 128x64 displays)(segments)
    oled_command(0xB0 | row);  // 0xB0 is the base address for page 0.
    oled_command(0x00 | (col& 0x0F));
    oled_command(0x10 | (col >> 4));
}
