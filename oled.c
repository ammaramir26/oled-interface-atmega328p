#include "oled.h"
void i2c_init(void) {
    TWSR = 0x00; // Set pre-scaler bits to zero
    TWBR = 0x62; // 75 kHz for XTAL = 16MHz
    TWCR = 0x04; // Enable the TWI module
}

void i2c_start(void) {
    TWCR = (1 << TWINT) | (1 << TWSTA) | (1 << TWEN);
    while ((TWCR & (1 << TWINT)) == 0);
}

void i2c_stop(void) {
    TWCR = (1 << TWINT) | (1 << TWEN) | (1 << TWSTO);
}

void i2c_write(unsigned char data) {
    TWDR = data;
    TWCR = (1 << TWINT) | (1 << TWEN);
    while ((TWCR & (1 << TWINT)) == 0);
}

void oled_command(char cmnd) {
    i2c_start();
    i2c_write(0x78); // Address + write
    i2c_write(0x00); // Continuation bit 0 +cmnd0
    i2c_write(cmnd);
    i2c_stop();
}

void oled_data(char data) {
    i2c_start();
    i2c_write(0x78); // Address + write
    i2c_write(0x40); // Data at GDDRAM
    i2c_write(data);
    i2c_stop();
}

